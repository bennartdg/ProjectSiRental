  private void loadData() {
    memberService = new MemberServiceImpl();
    List<Member> listMember;

    listMember = memberService.findAll();
    Object[][] objectMember = new Object[listMember.size()][5];

    int counter = 0;

    for (Member member : listMember) {
      objectMember[counter][0] = member.getIdMember();
      objectMember[counter][1] = member.getNama();
      objectMember[counter][2] = member.getJenisKelamin();
      objectMember[counter][3] = member.getAlamat();
      objectMember[counter][4] = member.getTelepon();
      counter++;
    }
    tblMember.setModel(new javax.swing.table.DefaultTableModel(
            objectMember, new String[]{
              "ID Member", "Nama", "Jenis Kelamin", "Alamat", "Telepon"
            }
    ));
    
    double saldoAdmin = admin.getSaldo();
    txtSaldoAdmin.setText(saldoAdmin + "");
  }

