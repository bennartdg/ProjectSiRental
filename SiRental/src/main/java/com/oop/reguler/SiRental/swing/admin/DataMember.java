  private void tblMemberMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMemberMouseClicked
    String nama, alamat, telepon;
    char charJenisKelamin;
    int idMember;

    int row = tblMember.getSelectedRow();
    idMember = Integer.parseInt(tblMember.getValueAt(row, 0).toString());
    nama = tblMember.getValueAt(row, 1).toString();
    charJenisKelamin = tblMember.getValueAt(row, 2).toString().charAt(0);
    alamat = tblMember.getValueAt(row, 3).toString();
    telepon = tblMember.getValueAt(row, 4).toString();

    txtIdMember.setText(idMember + "");
    txtNama.setText(nama);
    txtAlamat.setText(alamat);
    txtTelepon.setText(telepon);
    if (charJenisKelamin == 'L') {
      txtJenisKelamin.setText("Laki-laki");
    } else {
      txtJenisKelamin.setText("Perempuan");
    }
  }//GEN-LAST:event_tblMemberMouseClicked

  private void btnLogoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogoutActionPerformed
    int dialogButton = JOptionPane.YES_NO_OPTION;

    int dialogResult = JOptionPane.showConfirmDialog(null, "yakin mau Logout nih?", "Warning", dialogButton);
    if (dialogResult == JOptionPane.YES_OPTION) {
      LandingPage landingPage = new LandingPage();
      admin.setLoginStatus(false);
      landingPage.setVisible(true);
      close();
    }
  }//GEN-LAST:event_btnLogoutActionPerformed

