  private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
    String merk;

    merk = txtCariMobil.getText();

    if (txtCariMobil.getText().equals("") || txtCariMobil.getText().equals("Merk...")) {
      JOptionPane.showMessageDialog(null, "Masukkan merk yang ingin dicari terlebih dahulu!");
    } else {
      emptyField();
      mobilServiceImpl = new MobilServiceImpl();
      List<Mobil> listMobil;
      listMobil = mobilServiceImpl.findByMerk(merk);
      Object[][] objectMobil = new Object[listMobil.size()][10];

      int counter = 0;

      for (Mobil mobil : listMobil) {
        objectMobil[counter][0] = mobil.getIdMobil();
        objectMobil[counter][1] = mobil.getMember().getIdMember();
        objectMobil[counter][2] = mobil.getPlatNo();
        objectMobil[counter][3] = mobil.getMerk();
        objectMobil[counter][4] = mobil.getWarna();
        objectMobil[counter][5] = mobil.getJenis();
        objectMobil[counter][6] = mobil.getKapasitas();
        objectMobil[counter][7] = mobil.getTransmisi();
        objectMobil[counter][8] = mobil.getTahunKeluar();
        objectMobil[counter][9] = mobil.getHarga();
        counter++;
      }
      tblCariMobil.setModel(new javax.swing.table.DefaultTableModel(
              objectMobil, new String[]{
                "ID Mobil", "ID Member", "Plat No", "Merk", "Warna", "Jenis", "Kapasitas", "Transmisi", "Tahun", "Harga"
              }
      ));
    }
  }//GEN-LAST:event_btnCariActionPerformed

