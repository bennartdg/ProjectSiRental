  private void btnPilihMobilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihMobilActionPerformed
    Transaksi transaksi = new Transaksi();
    int lamaSewa;
    double harga, pajak, hargaDurasi, totalHarga;
    String tanggalSewa;
    int dateChecker;

    //TODO: Bikin Exception chooserTanggal
    Date date = chooserTanggalSewa.getDate();
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");

    if (txtIdMobil.getText().equals("") && txtHarga.getText().equals("")) {
      JOptionPane.showMessageDialog(null, "Pilih terlebih dahulu Data pada Tabel!");
    } else {
      if (!txtLamaSewa.getText().equals("")) {
        tanggalSewa = dateFormat.format(date);
        harga = Double.parseDouble(txtHarga.getText());
        lamaSewa = Integer.parseInt(txtLamaSewa.getText());
        pajak = hitungPajak(harga);
        hargaDurasi = hitungHargaDurasi(harga, lamaSewa);
        totalHarga = hitungTotalHarga(pajak, hargaDurasi);

        txtTglSewa.setText(tanggalSewa);
        txtLamaSewaKonfirm.setText(lamaSewa + "");
        txtPajak.setText(pajak + "");
        txtHargaDurasi.setText(hargaDurasi + "");
        txtTotalHarga.setText(totalHarga + "");
      } else {
        JOptionPane.showMessageDialog(null, "Isi Lama Sewa dan Pilih Tanggal Pesan terlebih dahulu!");
      }
    }
  }//GEN-LAST:event_btnPilihMobilActionPerformed
  private double hitungPajak(double harga) {
    return 0.1 * harga;
  }

  private double hitungHargaDurasi(double harga, int lamaSewa) {
    return harga * lamaSewa;
  }

  private double hitungTotalHarga(double pajak, double hargaDurasi) {
    return pajak + hargaDurasi;
  }


