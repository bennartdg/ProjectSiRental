  private void btnSewaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSewaActionPerformed
    TransaksiService transaksiService = new TransaksiServiceImpl();
    TransaksiServiceImpl transaksiServiceImpl = new TransaksiServiceImpl();
    MobilServiceImpl mobilServiceImpl = new MobilServiceImpl();
    CustomerServiceImpl customerServiceImpl = new CustomerServiceImpl();

    Transaksi transaksi = new Transaksi();
    Member member = new Member();
    Mobil mobil = new Mobil();

    int idMobil, idMember, lamaPeminjaman;
    String tanggalPesan;
    double pajak, hargaDurasi, totalHarga;

    if (txtIdMobil.getText().equals("") && txtHarga.getText().equals("")) {
      JOptionPane.showMessageDialog(null, "Pilih terlebih dahulu Data pada Tabel!");
    } else {
      if (!txtLamaSewa.getText().equals("") || !txtTglSewa.getText().equals("")) {
        idMobil = Integer.parseInt(txtIdMobil.getText());
        idMember = Integer.parseInt(txtIdMember.getText());
        tanggalPesan = txtTglSewa.getText();
        lamaPeminjaman = Integer.parseInt(txtLamaSewaKonfirm.getText());
        pajak = Double.parseDouble(txtPajak.getText());
        hargaDurasi = Double.parseDouble(txtHargaDurasi.getText());
        totalHarga = Double.parseDouble(txtTotalHarga.getText());

        mobil.setIdMobil(idMobil);
        member.setIdMember(idMember);

        transaksi.setMobil(mobil);
        transaksi.setMember(member);
        transaksi.setCostumer(customer);
        transaksi.setTanggalPesan(tanggalPesan);
        transaksi.setLamaPeminjaman(lamaPeminjaman);
        transaksi.setPajak(pajak);
        transaksi.setHargaDurasi(hargaDurasi);
        transaksi.setTotalHarga(totalHarga);

        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Anda yakin untuk melakukan Transaksi?", "Warning", dialogButton);

        if (dialogResult == JOptionPane.YES_OPTION) {
          double saldoCustomer;

          transaksiService.create(transaksi);

          saldoCustomer = customer.getSaldo() - transaksi.getTotalHarga();
          customer.setSaldo(saldoCustomer);
          customerServiceImpl.saldoPengurangan(customer);

          transaksiServiceImpl.updateSaldoAdmin(transaksi);

          transaksiServiceImpl.updateSaldoMember(transaksi);

          mobilServiceImpl.updateStatusOff(idMobil);

          JOptionPane.showMessageDialog(null, "Mobil berhasil diSewa!");

          loadData();
          emptyField();
        }
      } else {
        JOptionPane.showMessageDialog(null, "Isi Lama Sewa dan Pilih Tanggal Pesan terlebih dahulu!");
      }
    }
  }//GEN-LAST:event_btnSewaActionPerformed

