  private void loadData() {
    transaksiService = new TransaksiServiceImpl();
    List<Transaksi> listTransaksi;

    listTransaksi = transaksiService.findAll();
    Object[][] objectTransaksi = new Object[listTransaksi.size()][11];

    int counter = 0;

    for (Transaksi transaksi : listTransaksi) {
      objectTransaksi[counter][0] = transaksi.getIdTransaksi();
      objectTransaksi[counter][1] = transaksi.getMobil().getIdMobil();
      objectTransaksi[counter][2] = transaksi.getMember().getIdMember();
      objectTransaksi[counter][3] = transaksi.getCustomer().getIdCustomer();
      objectTransaksi[counter][4] = transaksi.getTanggalPesan();
      objectTransaksi[counter][5] = transaksi.getTanggalKembali();
      objectTransaksi[counter][6] = transaksi.getLamaPeminjaman();
      objectTransaksi[counter][7] = transaksi.getPajak();
      objectTransaksi[counter][8] = transaksi.getHargaDurasi();
      objectTransaksi[counter][9] = transaksi.getTotalHarga();
      objectTransaksi[counter][10] = transaksi.getStatus();
      counter++;
    }

    tblTransaksi.setModel(new javax.swing.table.DefaultTableModel(
            objectTransaksi, new String[]{
              "ID Transaksi", "ID Mobil", "ID Member", "ID Customer",
              "Pesan", "Kembali", "Lama", "Pajak", "Harga Durasi", "Total Harga", "Status"
            }
    ));

    double saldoAdmin = admin.getSaldo();
    txtSaldoAdmin.setText(saldoAdmin + "");
  }

