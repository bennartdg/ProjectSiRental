  private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    int id;
    if (txtIdTransaksi.getText().equals("")) {
      JOptionPane.showMessageDialog(null, "Pilih data terlebih dahulu!");
    } else {

      transaksiService = new TransaksiServiceImpl();
      int dialogButton = JOptionPane.YES_NO_OPTION;

      id = Integer.parseInt(txtIdTransaksi.getText());
      int dialogResult = JOptionPane.showConfirmDialog(null, "Anda yakin untuk menghapus?", "Warning", dialogButton);
      if (dialogResult == JOptionPane.YES_OPTION) {
        transaksiService.delete(id);
        loadData();
        emptyField();
      }
    }
  }//GEN-LAST:event_btnDeleteActionPerformed

